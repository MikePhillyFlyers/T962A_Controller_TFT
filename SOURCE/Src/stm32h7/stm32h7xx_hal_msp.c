/**
  * @brief JPEG MSP Initialization
  *        This function configures the hardware resources used in this example:
  *           - Peripheral's clock enable
  *           - NVIC configuration for JPEG interrupt request enable
  *           - DMA configuration for transmission request by peripheral
  *           - NVIC configuration for DMA interrupt request enable
  * @param hjpeg: JPEG handle pointer
  * @retval None
  */
void HAL_JPEG_MspInit(JPEG_HandleTypeDef *hjpeg)
{
    MDMA_HandleTypeDef* pMdmaIn = &g_PeriphCtrl.hmdmaIn;    
    MDMA_HandleTypeDef* pMdmaOut = &g_PeriphCtrl.hmdmaOut;    
    
    /* Enable JPEG clock */
    __HAL_RCC_JPGDECEN_CLK_ENABLE();
    
    /* Enable MDMA clock */
    __HAL_RCC_MDMA_CLK_ENABLE();
    
    /* NO INTERRUPTS NEEDED FOR NOW */
    //HAL_NVIC_SetPriority(JPEG_IRQn, 0x07, 0x0F);
    //HAL_NVIC_EnableIRQ(JPEG_IRQn);  
    
    /* Input MDMA */
    /* Set the parameters to be configured */ 
    pMdmaIn->Init.Priority           = MDMA_PRIORITY_HIGH;
    pMdmaIn->Init.Endianness         = MDMA_LITTLE_ENDIANNESS_PRESERVE;
    pMdmaIn->Init.SourceInc          = MDMA_SRC_INC_BYTE;
    pMdmaIn->Init.DestinationInc     = MDMA_DEST_INC_DISABLE;
    pMdmaIn->Init.SourceDataSize     = MDMA_SRC_DATASIZE_BYTE;
    pMdmaIn->Init.DestDataSize       = MDMA_DEST_DATASIZE_WORD;
    pMdmaIn->Init.DataAlignment      = MDMA_DATAALIGN_PACKENABLE;   
    pMdmaIn->Init.SourceBurst        = MDMA_SOURCE_BURST_32BEATS;
    pMdmaIn->Init.DestBurst          = MDMA_DEST_BURST_16BEATS; 
    pMdmaIn->Init.SourceBlockAddressOffset = 0;
    pMdmaIn->Init.DestBlockAddressOffset  = 0;
    
    /*Using JPEG Input FIFO Threshold as a trigger for the MDMA*/
    pMdmaIn->Init.Request = MDMA_REQUEST_JPEG_INFIFO_TH; /* Set the MDMA HW trigger to JPEG Input FIFO Threshold flag*/  
    pMdmaIn->Init.TransferTriggerMode = MDMA_BUFFER_TRANSFER;  
    pMdmaIn->Init.BufferTransferLength = 32; /*Set the MDMA buffer size to the JPEG FIFO threshold size i.e 32 bytes (8 words)*/
    
    pMdmaIn->Instance = MDMA_Channel7;
    
    /* Associate the DMA handle */
    __HAL_LINKDMA(hjpeg, hdmain, *pMdmaIn);
    
    /* DeInitialize the DMA Stream */
    HAL_MDMA_DeInit(pMdmaIn);  
    /* Initialize the DMA stream */
    HAL_MDMA_Init(pMdmaIn);
    
    
    /* Output MDMA */
    /* Set the parameters to be configured */ 
    pMdmaOut->Init.Priority        = MDMA_PRIORITY_VERY_HIGH;
    pMdmaOut->Init.Endianness      = MDMA_LITTLE_ENDIANNESS_PRESERVE;
    pMdmaOut->Init.SourceInc       = MDMA_SRC_INC_DISABLE;
    pMdmaOut->Init.DestinationInc  = MDMA_DEST_INC_BYTE;
    pMdmaOut->Init.SourceDataSize  = MDMA_SRC_DATASIZE_WORD;
    pMdmaOut->Init.DestDataSize    = MDMA_DEST_DATASIZE_BYTE;
    pMdmaOut->Init.DataAlignment   = MDMA_DATAALIGN_PACKENABLE;
    pMdmaOut->Init.SourceBurst     = MDMA_SOURCE_BURST_32BEATS;
    pMdmaOut->Init.DestBurst       = MDMA_DEST_BURST_32BEATS;  
    pMdmaOut->Init.SourceBlockAddressOffset = 0;
    pMdmaOut->Init.DestBlockAddressOffset  = 0;
    
    
    /*Using JPEG Output FIFO Threshold as a trigger for the MDMA*/
    pMdmaOut->Init.Request              = MDMA_REQUEST_JPEG_OUTFIFO_TH; /* Set the MDMA HW trigger to JPEG Output FIFO Threshold flag*/ 
    pMdmaOut->Init.TransferTriggerMode  = MDMA_BUFFER_TRANSFER;    
    pMdmaOut->Init.BufferTransferLength = 32; /*Set the MDMA buffer size to the JPEG FIFO threshold size i.e 32 bytes (8 words)*/
    
    pMdmaOut->Instance = MDMA_Channel6;
    /* DeInitialize the DMA Stream */
    HAL_MDMA_DeInit(pMdmaOut);  
    /* Initialize the DMA stream */
    HAL_MDMA_Init(pMdmaOut);
    
    /* Associate the DMA handle */
    __HAL_LINKDMA(hjpeg, hdmaout, *pMdmaOut);
    
    /* INTERRUPTS NOT NEEDED FOR NOW!! */
    //HAL_NVIC_SetPriority(MDMA_IRQn, 0x08, 0x0F);
    //HAL_NVIC_EnableIRQ(MDMA_IRQn);
  
}

